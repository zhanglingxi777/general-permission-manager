<template>
  <div class="login-container">
    <el-form ref="loginForm" :model="loginForm" :rules="loginRules" class="login-form" autocomplete="on"
             label-position="left">

      <div class="title-container">
        <h3 class="title">新闻发布系统</h3>
      </div>

      <el-form-item prop="username">
        <span class="svg-container">
          <svg-icon icon-class="user"/>
        </span>
        <el-input
            ref="username"
            v-model="loginForm.username"
            placeholder="请输入用户名"
            name="username"
            type="text"
            tabindex="1"
            autocomplete="on"
        />
      </el-form-item>

      <el-tooltip v-model="capsTooltip" content="Caps lock is On" placement="right" manual>
        <el-form-item prop="password">
          <span class="svg-container">
            <svg-icon icon-class="password"/>
          </span>
          <el-input
              :key="passwordType"
              ref="password"
              v-model="loginForm.password"
              :type="passwordType"
              placeholder="请输入密码"
              name="password"
              tabindex="2"
              autocomplete="on"
              @keyup.native="checkCapslock"
              @blur="capsTooltip = false"
              @keyup.enter.native="handleLogin"
          />
          <span class="show-pwd" @click="showPwd">
            <svg-icon :icon-class="passwordType === 'password' ? 'eye' : 'eye-open'"/>
          </span>
        </el-form-item>
      </el-tooltip>

      <el-row :gutter="10">
        <el-col :span="18">
          <el-form-item prop="verifyCode">
            <span class="svg-container">
              <svg-icon icon-class="star"/>
            </span>
            <el-input placeholder="请输入验证码" v-model="loginForm.verifyCode"></el-input>
          </el-form-item>
        </el-col>
        <el-col :span="6">
          <el-image style="width: 100%;height: 50px;cursor: pointer;" @click="getVerifyImage" :src="verifyImage" />
        </el-col>
      </el-row>

      <el-row style="margin: 0 0 20px">
        <el-col :span="2">
          <el-checkbox v-model="loginForm.rememberMe">记住我</el-checkbox>
        </el-col>
      </el-row>
      <el-button :loading="loading" type="primary" style="width:100%;margin-bottom:30px;"
                 @click.native.prevent="handleLogin">登录
      </el-button>
    </el-form>
  </div>
</template>

<script>
import SocialSign from './components/SocialSignin'
import {encrypt} from '@/utils/aes'
import {getVcImage} from "@/api/system/user";

export default {
  name: 'Login',
  components: {SocialSign},
  data() {
    return {
      loginForm: {
        username: '',
        password: '',
        verifyCode: '',
        uuid: '',
        rememberMe: false
      },
      loginRules: {
        username: [{required: true, trigger: 'blur', message: "用户名不能为空"}],
        password: [{required: true, trigger: 'blur', message: "密码不能为空"}],
        verifyCode: [{required: true, trigger: 'blur', message: "验证码不能为空"}]
      },
      passwordType: 'password',
      capsTooltip: false,
      loading: false,
      showDialog: false,
      redirect: undefined,
      otherQuery: {},
      verifyImage: ''
    }
  },
  watch: {
    $route: {
      handler: function (route) {
        const query = route.query
        if (query) {
          this.redirect = query.redirect
          this.otherQuery = this.getOtherQuery(query)
        }
      },
      immediate: true
    }
  },
  mounted() {
    this.getVerifyImage()
    if (this.loginForm.username === '') {
      // 如果用户名为空 焦点聚焦到用户名输入框
      this.$refs.username.focus()
    } else if (this.loginForm.password === '') {
      this.$refs.password.focus()
    }
  },
  methods: {
    // 获取验证码图片
    getVerifyImage() {
      getVcImage().then(response => {
        this.verifyImage = response.data.image
        this.loginForm.uuid = response.data.uuid
      })
    },
    checkCapslock(e) {
      const {key} = e
      this.capsTooltip = key && key.length === 1 && (key >= 'A' && key <= 'Z')
    },
    showPwd() {
      if (this.passwordType === 'password') {
        this.passwordType = ''
      } else {
        this.passwordType = 'password'
      }
      this.$nextTick(() => {
        this.$refs.password.focus()
      })
    },
    handleLogin() {
      this.$refs.loginForm.validate(valid => {
        if (valid) {
          this.loading = true
          this.loginForm.password = encrypt(this.loginForm.password)
          this.$store.dispatch('user/login', this.loginForm)
              .then(() => {
                this.$router.push({path: this.redirect || '/', query: this.otherQuery})
                this.loading = false
              })
              .catch(() => {
                this.loading = false
              })
        } else {
          console.log('error submit!!')
          return false
        }
      })
    },
    getOtherQuery(query) {
      return Object.keys(query).reduce((acc, cur) => {
        if (cur !== 'redirect') {
          acc[cur] = query[cur]
        }
        return acc
      }, {})
    },
  }
}
</script>

